package org.geomajas.plugin.deskmanager.command.security.dto;

import org.geomajas.command.CommandRequest;

/**
 * Request for {@link org.geomajas.plugin.deskmanager.command.security.CreateUserCommand}.
 * 
 * @author Jan De Moerloose
 * 
 */
public class CreateUserRequest implements CommandRequest {

	private static final long serialVersionUID = 115L;

	public static final String COMMAND = "CreateUser";

	private String email;

	private String name;

	private String surname;

	private String password;

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSurname() {
		return surname;
	}

	public void setSurname(String surname) {
		this.surname = surname;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

}
